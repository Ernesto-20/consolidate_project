class ADGroup:
    def __init__(self, name_a, code_a, name_d, code_d):
        self.name_a = name_a
        self.code_a = code_a
        self.name_d = name_d
        self.code_d = code_d

    def get_name_a(self): return self.name_a

    def get_code_a(self): return self.code_a

    def get_name_d(self): return self.name_d

    def get_code_d(self): return self.code_d